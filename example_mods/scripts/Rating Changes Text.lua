function onUpdate()

    if ratingName == 'Perfect!!' then

        setRatingName('Bonk!!')

    end

    if ratingName == 'Sick!' then

        setRatingName('Good Job!')

    end

    if ratingName == 'Great' then

        setRatingName('Great')

    end

    if ratingName == 'Good' then

        setRatingName('Meh.')

    end

    if ratingName == 'Meh' then

        setRatingName('Bad')

    end

    if ratingName == 'You Suck' then

        setRatingName('Really Loser')

    end

end